
// Point class is used to store x--coordinates and y-- coordinates of the hubs
// the object of Point class is then used in the addDistributionHub method
public class Point {
    private int x;
    private int y;

    public void setX(int x){
        this.x = x;
    }

    public void setY(int y){
        this.y =y;
    }

    public int getX(){
        return this.x;
    }

    public int getY(){
        return this.y;
    }
}
